package zad1;

public class Towar {
    private int id;
    private int waga;

    public Towar(int id, int waga) {
        this.id = id;
        this.waga = waga;
    }

    public int getthisId() {
        return id;
    }

    public int getWaga() {
        return waga;
    }
}
